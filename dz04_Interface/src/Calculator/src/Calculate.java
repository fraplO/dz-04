public class Calculate
{
public static int _sum(int b,int c)
{
    return b+c;
}
    public static int _minus(int b,int c)
    {
        return b-c;
    }
    public static int _del(int b,int c)
    {
        return b/c;
    }
    public static int _multi(int b,int c)
    {
        return b*c;
    }
    public static double _degree(int b, int c)
    {
        return Math.pow(b,c);
    }
}
